import json

# 读取文件
with open('系统部署/services/skill_bridge/config/content_generator.json', 'r') as f:
    data = json.load(f)

# 检查是否已有首评方法论
template = data['steps'][0]['llm_prompt_template']
if '首评生成方法论' in template:
    print('✅ 首评方法论已存在，无需重复添加')
else:
    # 首评生成方法论内容
    first_comment_methodology = """

================================================================================
【首评生成方法论：替观众发声】

## 核心原则
你必须始终扮演"替观众说话的嘴替"角色，而非"询问观众的记者"。

**核心理念**：
- ❌ 我在询问你 → ✅ 我替你说
- ❌ 你有什么问题 → ✅ 你不敢说的话我说
- ❌ 帮我评论一下 → ✅ 我说出了你想的
- ❌ 你是观众 → ✅ 我是嘴替

**核心公式**：
首评 = 替观众说出来 × 说准了 × 说得比观众更好

## 心理学原理

| 心理效应 | 解释 | 效果 |
|----------|------|------|
| 被理解感 | "他怎么知道我在想什么" | 立即关注 |
| 群体认同 | "原来大家都是这么想的" | 打破沉默 |
| 去羞耻化 | "不是我暴露，是他替我说" | 降低顾虑 |
| 情感释放 | "说出了我憋在心里的话" | 引发共鸣 |
| 转发动机 | "说的太对了，要转给朋友看" | 病毒传播 |

## 替观众发声的六种类型

### 类型A：替观众说出心里话
公式：替观众说出憋在心里的话 + 制造群体认同 + 低门槛回应
示例：当妈的太难了！宝宝不舒服又不知道该怪谁——老公？产品？还是自己没照顾好？姐妹们，有没有这种无力感？

### 类型B：替观众怼回去
公式：替观众表达不满 + 制造群体共鸣 + 低门槛认同
示例：真的想怼某些人：人家怎么养娃关你什么事？姐妹们，你们被"道德绑架"过吗？

### 类型C：替观众做出判断
公式：替观众做出评价 + 提供判断依据 + 低门槛认同
示例：说句公道话：这件事根本不是贵=好！姐妹们，你们被"常识"骗过吗？

### 类型D：替观众表达情绪
公式：替观众发泄情绪 + 承认情绪合理性 + 低门槛共鸣
示例：憋了这么久的话：当妈的真的太难了！你们是不是也这种感觉？

### 类型E：替观众做出表态
公式：替观众表明立场 + 制造阵营感 + 低门槛站队
示例：不吹不黑，我的态度是___。姐妹们，你们站哪边？

### 类型F：替观众说出经历
公式：先承认经历相似 + 替观众说出感受 + 低门槛认同
示例：我家之前也是这样，后来___。你们家有没有同款情况？

## 首评生成规则（强制执行）

### ✅ 推荐句式
- "这种情况其实很常见，___"
- "憋了这么久的话：___"
- "真的想怼：___"
- "当妈的都懂这种感觉：___"
- "不吹不黑，我的态度是___"
- "姐妹们，你们有没有同感？"
- "有同感的点个赞让我看看有多少"
- "是不是你？说中了的举手"
- "太真实了，说的就是你"

### ❌ 禁止句式（询问暴露型）
- "你家宝宝有这种情况吗？"（让用户暴露问题）
- "你们有什么问题想问？"（居高临下）
- "评论区告诉我你们的情况"（索取信息）
- "有什么问题可以问我"（太官方）
- "欢迎留言讨论"（太泛泛）
- "你的宝宝是否也有类似问题？"（直接暴露）

### 禁止原因
这些问法的问题：
1. 把用户推到"被审视"的弱势位置
2. 用户需要暴露自己或孩子的问题
3. 社会比较焦虑让用户选择沉默
4. 参与门槛高，需要组织语言回答

## 不同内容类型的首评策略

| 内容类型 | 替观众发声的切入口 | 典型首评 |
|----------|-------------------|----------|
| 痛点问题 | 替观众说出无助感 | "当妈的太难了..." |
| 解决方案 | 替观众做出判断 | "不吹不黑，这方法..." |
| 知识科普 | 替观众问出疑问 | "其实我一直想问..." |
| 对比测评 | 替观众做选择 | "别再被忽悠了..." |
| 人设故事 | 替观众表达情绪 | "说的太对了..." |
| 争议话题 | 替观众怼回去 | "真想怼一句..." |
| 社会现象 | 替观众说出来 | "大家都是敢怒不敢言..." |

## 首评质量检查清单

生成首评后，必须检查：
- [ ] 这句话是不是观众想说但不敢说的？
- [ ] 观众看到会不会觉得"他懂我"？
- [ ] 观众是否不需要暴露自己就能参与？
- [ ] 是否制造了"原来大家都是这么想的"的感觉？
- [ ] 回应门槛是否足够低（扣1/点赞/举手/太对了）？
- [ ] 是否有替观众"说心里话/怼回去/表态"的元素？
"""

    # 获取 llm_prompt_template
    steps = data.get('steps', [])
    for step in steps:
        if step.get('id') == 'step_generate_content':
            template = step.get('llm_prompt_template', '')
            # 在【内容深度强制要求】之后添加首评方法论
            marker = "================================================================================\n【JSON输出格式"
            if marker in template:
                new_template = template.replace(marker, first_comment_methodology + marker)
                step['llm_prompt_template'] = new_template
                print("✅ 首评方法论已添加到 llm_prompt_template")
            else:
                print("❌ 未找到插入位置标记")
            break

    # 写入文件
    with open('系统部署/services/skill_bridge/config/content_generator.json', 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    # 验证
    try:
        with open('系统部署/services/skill_bridge/config/content_generator.json', 'r') as f:
            json.load(f)
        print("✅ JSON 文件验证通过")
    except json.JSONDecodeError as e:
        print(f"❌ JSON 文件验证失败: {e}")
