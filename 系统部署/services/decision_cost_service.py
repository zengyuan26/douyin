    def diagnose(self, business_keyword: str, adjustments: Dict = None) -> Dict:
        """
        诊断业务决策成本

        Args:
            business_keyword: 业务关键词
            adjustments: 用户调整的维度评分

        Returns:
            诊断结果字典
        """
        # 匹配业务类型
        matched_business = self.match_business(business_keyword)

        if not matched_business:
            return {
                "success": False,
                "message": f"暂不支持该业务类型: {business_keyword}",
                "suggestion": "请尝试：医美、装修、留学、高考志愿、金融投资、儿童教育、婚恋相亲、房产买卖等",
            }

        # 获取知识图谱
        business_data = self.knowledge_graph[matched_business]
        base_scores = business_data["成本评分"]

        # 应用用户调整
        if adjustments:
            for dim, score in adjustments.items():
                if dim in base_scores and 1 <= score <= 10:
                    base_scores[dim] = score

        # 计算综合评分
        scores = list(base_scores.values())
        avg_score = round(sum(scores) / len(scores), 1)
        max_score = max(scores)
        min_score = min(scores)

        # 判断内容价值
        if avg_score >= 8:
            content_value = "极高价值"
            content_emoji = "💎"
        elif avg_score >= 7:
            content_value = "高价值"
            content_emoji = "✅"
        elif avg_score >= 6:
            content_value = "中等价值"
            content_emoji = "⚠️"
        else:
            content_value = "低价值"
            content_emoji = "❌"

        # 构建维度详情
        dimensions = []
        for dim_name, dim_score in base_scores.items():
            config = self.dimensions_config.get(dim_name, {})
            dimensions.append({
                "name": dim_name,
                "score": dim_score,
                "icon": config.get("icon", "📊"),
                "description": config.get("description", ""),
                "system_able": config.get("system_able", True),
                "llm_needed": config.get("llm_needed", False),
                "search_needed": config.get("search_needed", False),
            })

        # 按评分排序
        dimensions.sort(key=lambda x: x["score"], reverse=True)

        # 获取推荐的内容方向
        content_directions = []
        for cd in business_data.get("内容方向", []):
            content_directions.append({
                "type": cd["type"],
                "weight": cd["weight"],
                "icon": self._get_direction_icon(cd["type"]),
                "title_templates": cd.get("title_templates", []),
                "keywords": cd.get("keywords", []),
            })

        # 按权重排序
        content_directions.sort(key=lambda x: x["weight"], reverse=True)

        # 构建最终结果
        result = {
            "success": True,
            "business_type": matched_business,
            "input_keyword": business_keyword,
            "dimensions": dimensions,
            "scores": base_scores,
            "avg_score": avg_score,
            "max_score": max_score,
            "min_score": min_score,
            "content_value": content_value,
            "content_emoji": content_emoji,
            "risk_level": business_data["基础特征"]["风险等级"],
            "reversibility": business_data["基础特征"]["可逆性"],
            "target_users": business_data["基础特征"]["目标用户"],
            "decision_cycle": business_data["基础特征"]["决策周期"],
            "price_range": business_data["基础特征"]["价格区间"],
            "core_anxieties": business_data["核心焦虑"],
            "content_directions": content_directions,
            "all_business_types": list(self.knowledge_graph.keys()),
        }

        return result

